  "# ============================================================",
  "# 3) NORMALISER LES TYPES DU REGISTRE",
  "# ============================================================",
  "",
  "normalize_registry_types <- function(df) {",
  "",
  "  empty <- tibble::tibble(",
  "    sitrep_no    = integer(),",
  "    pdf_url      = character(),",
  "    date_raw     = character(),",
  "    link_text    = character(),",
  "    year_in_url  = integer(),",
  "    source_page  = character(),",
  "    epidemic     = character(),",
  "    scraped_at   = character(),",
  "    downloaded   = logical(),",
  "    extracted    = logical(),",
  "    analysed     = logical(),",
  "    local_pdf    = character(),",
  "    first_seen   = character(),",
  "    last_updated = character()",
  "  )",
  "",
  "  if (is.null(df) || nrow(df) == 0) {",
  "    return(empty)",
  "  }",
  "",
  "  for (col in names(empty)) {",
  "    if (!col %in% names(df)) {",
  "      df[[col]] <- NA",
  "    }",
  "  }",
  "",
  "  df <- df %>%",
  "    dplyr::mutate(",
  "      sitrep_no   = suppressWarnings(as.integer(sitrep_no)),",
  "      year_in_url = suppressWarnings(as.integer(year_in_url)),",
  "",
  "      pdf_url     = as.character(pdf_url),",
  "      date_raw    = as.character(date_raw),",
  "      link_text   = as.character(link_text),",
  "      source_page = as.character(source_page),",
  "      epidemic    = as.character(epidemic),",
  "      local_pdf   = as.character(local_pdf),",
  "",
  "      scraped_at   = as.character(scraped_at),",
  "      first_seen   = as.character(first_seen),",
  "      last_updated = as.character(last_updated),",
  "",
  "      downloaded = dplyr::case_when(",
  "        is.na(downloaded) ~ FALSE,",
  "        as.character(downloaded) %in% c(\"TRUE\", \"true\", \"1\", \"Yes\", \"yes\") ~ TRUE,",
  "        TRUE ~ FALSE",
  "      ),",
  "      extracted = dplyr::case_when(",
  "        is.na(extracted) ~ FALSE,",
  "        as.character(extracted) %in% c(\"TRUE\", \"true\", \"1\", \"Yes\", \"yes\") ~ TRUE,",
  "        TRUE ~ FALSE",
  "      ),",
  "      analysed = dplyr::case_when(",
  "        is.na(analysed) ~ FALSE,",
  "        as.character(analysed) %in% c(\"TRUE\", \"true\", \"1\", \"Yes\", \"yes\") ~ TRUE,",
  "        TRUE ~ FALSE",
  "      )",
  "    ) %>%",
  "    dplyr::select(dplyr::all_of(names(empty)), dplyr::everything())",
  "",
  "  df",
  "}",
  "",
