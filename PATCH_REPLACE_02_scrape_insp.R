  "# ============================================================",
  "# 4) CHARGER LE REGISTRE",
  "# ============================================================",
  "",
  "load_registry <- function() {",
  "",
  "  empty <- normalize_registry_types(tibble::tibble())",
  "",
  "  if (!file.exists(REGISTRY_FP)) {",
  "    return(empty)",
  "  }",
  "",
  "  reg <- tryCatch(",
  "    readr::read_csv(",
  "      REGISTRY_FP,",
  "      col_types = readr::cols(",
  "        sitrep_no    = readr::col_integer(),",
  "        pdf_url      = readr::col_character(),",
  "        date_raw     = readr::col_character(),",
  "        link_text    = readr::col_character(),",
  "        year_in_url  = readr::col_integer(),",
  "        source_page  = readr::col_character(),",
  "        epidemic     = readr::col_character(),",
  "        scraped_at   = readr::col_character(),",
  "        downloaded   = readr::col_logical(),",
  "        extracted    = readr::col_logical(),",
  "        analysed     = readr::col_logical(),",
  "        local_pdf    = readr::col_character(),",
  "        first_seen   = readr::col_character(),",
  "        last_updated = readr::col_character(),",
  "        .default     = readr::col_character()",
  "      ),",
  "      show_col_types = FALSE",
  "    ),",
  "    error = function(e) {",
  "      cat(\"   WARNING: registry could not be read cleanly:\", conditionMessage(e), \"\\n\")",
  "      tibble::tibble()",
  "    }",
  "  )",
  "",
  "  reg <- normalize_registry_types(reg)",
  "",
  "  if (nrow(reg) > 0) {",
  "",
  "    fallback_source <- dplyr::case_when(",
  "      !is.na(reg$pdf_url) & reg$pdf_url != \"\" ~ reg$pdf_url,",
  "      !is.na(reg$source_page) & reg$source_page != \"\" ~ reg$source_page,",
  "      TRUE ~ NA_character_",
  "    )",
  "",
  "    reg$sitrep_no <- purrr::map_int(fallback_source, extract_sitrep_no)",
  "",
  "    reg <- reg %>%",
  "      dplyr::filter(!is.na(sitrep_no), sitrep_no >= 1) %>%",
  "      dplyr::arrange(dplyr::desc(sitrep_no))",
  "  }",
  "",
  "  reg",
  "}",
  "",
  "",
  "# ============================================================",
  "# 5) SAUVEGARDER LE REGISTRE",
  "# ============================================================",
  "",
  "save_registry <- function(registry) {",
  "",
  "  registry <- normalize_registry_types(registry)",
  "",
  "  readr::write_csv(",
  "    registry,",
  "    REGISTRY_FP,",
  "    na = \"\"",
  "  )",
  "}",
  "",
  "",
  "# ============================================================",
  "# 6) FUSIONNER SCRAPING + REGISTRE EXISTANT",
  "# ============================================================",
  "",
  "merge_registry <- function(scraped, registry) {",
  "",
  "  now <- as.character(Sys.time())",
  "",
  "  registry <- normalize_registry_types(registry)",
  "",
  "  scraped <- scraped %>%",
  "    dplyr::mutate(",
  "      sitrep_no    = suppressWarnings(as.integer(sitrep_no)),",
  "      year_in_url  = suppressWarnings(as.integer(year_in_url)),",
  "      pdf_url      = as.character(pdf_url),",
  "      date_raw     = as.character(date_raw),",
  "      link_text    = as.character(link_text),",
  "      source_page  = as.character(source_page),",
  "      epidemic     = as.character(epidemic),",
  "      scraped_at   = as.character(scraped_at),",
  "      downloaded   = FALSE,",
  "      extracted    = FALSE,",
  "      analysed     = FALSE,",
  "      local_pdf    = NA_character_,",
  "      first_seen   = now,",
  "      last_updated = now",
  "    ) %>%",
  "    normalize_registry_types()",
  "",
  "  known_urls <- registry$pdf_url",
  "",
  "  new_rows <- scraped %>%",
  "    dplyr::filter(!pdf_url %in% known_urls) %>%",
  "    normalize_registry_types()",
  "",
  "  pending <- registry %>%",
  "    dplyr::filter(",
  "      pdf_url %in% scraped$pdf_url,",
  "      is.na(downloaded) | downloaded == FALSE |",
  "        is.na(extracted) | extracted == FALSE",
  "    ) %>%",
  "    dplyr::mutate(last_updated = now) %>%",
  "    normalize_registry_types()",
  "",
  "  cat(\"   New SitReps detected:\", nrow(new_rows), \"\\n\")",
  "",
  "  if (nrow(pending) > 0) {",
  "    cat(\"   Pending incomplete:\", nrow(pending), \"\\n\")",
  "  }",
  "",
  "  merged <- dplyr::bind_rows(",
  "    normalize_registry_types(registry),",
  "    normalize_registry_types(new_rows)",
  "  ) %>%",
  "    normalize_registry_types() %>%",
  "    dplyr::distinct(pdf_url, .keep_all = TRUE) %>%",
  "    dplyr::arrange(dplyr::desc(sitrep_no))",
  "",
  "  new_or_pending <- dplyr::bind_rows(",
  "    normalize_registry_types(new_rows),",
  "    normalize_registry_types(pending)",
  "  ) %>%",
  "    normalize_registry_types() %>%",
  "    dplyr::distinct(pdf_url, .keep_all = TRUE) %>%",
  "    dplyr::arrange(dplyr::desc(sitrep_no))",
  "",
  "  list(",
  "    registry       = merged,",
  "    new_or_pending = new_or_pending",
  "  )",
  "}"
)

